export const actions = {
  async getPage({ commit }, param) {
    try {
      let req = await this.$axios.$get(`api/${param.url}`);
      return req.store;
    } catch (error) {
      console.log("an error occured", error);
    }
  },

  async getContact({ commit }, param) {
    try {
      let req = await this.$axios.$get(`api/page/url?name=contact`);
      commit("savecontact", req.store.page.attributes);
    } catch (error) {
      console.log("an error occured", error);
    }
  },

  async getNews({ commit }, param) {
    try {
      let req = await this.$axios.$post(`api/${param.url}`, {
        title: param.data
      });
      return req.store;
    } catch (error) {
      console.log("an error occured", error);
    }
  },

  async getAllBlog({ commit }, { param, url }) {
    try {
      let { data } = await this.$axios.get(
        `${url}${param.page}&limit=${param.offset}`
      );
      if (data.code == 200) {
        return data.store.list;
      }
    } catch (error) {
      console.log("An error occured", error);
    }
  },

  async Postdata({ commit }, param) {
    try {
      let req = await this.$axios.$post(`api/${param.url}`, param.data);
      return req.store;
    } catch (error) {
      console.log("an error occured", error);
      throw error;
    }
  }
};
